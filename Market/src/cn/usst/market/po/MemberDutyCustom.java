package cn.usst.market.po;

public class MemberDutyCustom {
	private String name;
	
	private String img;
	
	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	private String mainDuty;
	
	private String minorDuty;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMainDuty() {
		return mainDuty;
	}

	public void setMainDuty(String mainDuty) {
		this.mainDuty = mainDuty;
	}

	public String getMinorDuty() {
		return minorDuty;
	}

	public void setMinorDuty(String minorDuty) {
		this.minorDuty = minorDuty;
	}
	
	
}
