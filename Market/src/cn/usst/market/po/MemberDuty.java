package cn.usst.market.po;

public class MemberDuty {
	private int id;
	
	private int mainDutyId;
	
	private int minorDutyId;
	
	private int memberId;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getMainDutyId() {
		return mainDutyId;
	}

	public void setMainDutyId(int mainDutyId) {
		this.mainDutyId = mainDutyId;
	}

	public int getMinorDutyId() {
		return minorDutyId;
	}

	public void setMinorDutyId(int minorDutyId) {
		this.minorDutyId = minorDutyId;
	}

	public int getMemberId() {
		return memberId;
	}

	public void setMemberId(int memberId) {
		this.memberId = memberId;
	}
	
	
}
