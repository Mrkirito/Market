public class FinalCheckDTO {
    private String productName;
    private String priceAndSale;
    private String productPrice;
    private String salesSalary;
    private String worksSalary;
    private String isPhy;
    private String capacity;

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getPriceAndSale() {
        return priceAndSale;
    }

    public void setPriceAndSale(String priceAndSale) {
        this.priceAndSale = priceAndSale;
    }

    public String getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(String productPrice) {
        this.productPrice = productPrice;
    }

    public String getSalesSalary() {
        return salesSalary;
    }

    public void setSalesSalary(String salesSalary) {
        this.salesSalary = salesSalary;
    }

    public String getWorksSalary() {
        return worksSalary;
    }

    public void setWorksSalary(String worksSalary) {
        this.worksSalary = worksSalary;
    }

    public String getIsPhy() {
        return isPhy;
    }

    public void setIsPhy(String isPhy) {
        this.isPhy = isPhy;
    }

    public String getCapacity() {
        return capacity;
    }

    public void setCapacity(String capacity) {
        this.capacity = capacity;
    }
}
